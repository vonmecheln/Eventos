<H1>NORMAS PARA  INSCRIÇÃO E APRESENTAÇÃO DE TRABALHOS</h1>

<h2>Regulamento para Inscrição de Trabalhos</h2>

<ol>
  <li>Poderão participar do V SIMPÓSIO MÉDICO trabalhos técnicos e científicos voltados para a área da saúde.</li>
  <li>Os trabalhos deverão ser inscritos na forma de RESUMO EXPANDIDO.</li>
  <li>Os trabalhos poderão ser apresentados nas categorias PÔSTER ou APRESENTACÃO ORAL, devendo os autores escolher para qual categoria o resumo se destina no momento do envio. Os resumos não selecionados para apresentação na forma de Apresentacão Oral poderão ser avaliados para apresentação na forma de Pôster, desde que o autor faça essa opção no ato do envio.</li>
  <li>Os trabalhos inscritos estarão concorrendo ao prêmio de:
    <ol>
      <li>Melhor Pôster </li>
      <li>Melhor Apresentação Oral</li>
      <br/>
      O prêmio constará de certificado, explicitando a premiação.
    </ol>
  </li>
  
  <li>Os resumos expandidos serão encaminhados para a comissão científica, no site <a href="http://www.unioeste.br/simposiomedico">http://www.unioeste.br/simposiomedico</a> e deverão ser escritos em língua portuguesa, conforme as seguintes normas:
    <ol>
      <li>O resumo expandido deve ser redigido em documento no formato Word (.doc) papel A4 com a seguinte formatação de margens: superior 3,5 cm, inferior 3 cm, lateral esquerda 3,5 cm, lateral direita 3 cm. O número de páginas máximo é quatro.</li>
      <li>O resumo expandido dever conter: 
        <ul>
          <li>Título</li>
          <li>Nome dos autores</li>
          <li>Afiliação dos autores</li>
          <li>Resumo </li>
          <li>Palavras-chave</li>
          <li>Introdução</li>
          <li>Material e Métodos</li>
          <li>Resultados e Discussão</li>
          <li>Conclusões</li>
          <li>Agradecimentos (se for o caso)</li>
          <li>Referências</li>
        </ul>
      </li>
      <li>Os tópicos acima devem ser escritos em caixa alta, fonte Arial 12, negrito, alinhados à esquerda.</li> 
      <li>O título deve ser escrito em fonte Arial 12, em negrito.</li>
      <li>O corpo do texto deve ser escrito em fonte Arial 12, espaçamento simples.</li>
      <li>Logo após o nome dos autores deve ser colocado o e-mail de um dos autores do trabalho. O nome do apresentador deve ser sublinhado.</li>
      <li>O resumo deve ter no máximo 250 palavras. </li>
      <li>Devem ser incluídas no máximo três palavras-chave, fonte arial 12, normal, justificado, espaço simples. Evitar repetir palavras do título nas palavras-chave. </li>
      <li>O tópico "Material e Métodos"  pode ser substituído por "Revisão de literatura", se for o caso. </li>
      <li>Figuras, tabelas e fotos devem aparecer conforme são citadas no texto. A formatação para figuras e tabelas estão descritas no modelo de resumo.</li>
      <li>Em caso de dúvida verifique o modelo de resumo anexo. <a href="modelo/SIMPOSIO_2010_modelo_resumo_expandido.doc">Modelo de Resumo Expandido</a></li>
    </ol>
  </li>

  <li>Os trabalhos deverão ser entregues via Internet, no prazo de XX/07/2010 a 20/08/2010, sendo obrigatória a confirmação do pagamento da taxa de inscrição do Simpósio no momento do envio do mesmo, especificando a quais autores e/ou trabalhos está relacionado o pagamento para o e-mail: campea.unioeste@gmail.com, caso contrário o resumo estará automaticamente desclassificado.</li>

  <li>Cada autor inscrito no evento poderá apresentar até 02 (dois) trabalhos na categoria Pôster, e 01 (um) na categoria Apresentacão Oral.</li> 

  <li>Os autores autorizam a Comissão Organizadora do V SIMPÓSIO MÉDICO a publicar e divulgar o trabalho, não cabendo qualquer pagamento de direito autoral. </li>

  <li>Os trabalhos apresentados serão indexados nos Anais do V Simpósio Médico - ISSN 2175-3113 (divulgação em CD-ROM).</li>

  <li>A comunicação sobre a aprovação ou não, será através do correio eletrônico pelo qual foi remetido o trabalho, até o dia 26/08/2010.</li>

  <li>A Comissão Científica avaliará os trabalhos e selecionará somente aqueles que estiverem de acordo com as normas e tenham qualidade científica. Não caberá recurso à decisão da Comissão Avaliadora.</li>

  <li>Serão emitidos certificados:
    <ol>
      <li>A todos os participantes que tiverem no mínimo 85% de freqüência.</li> 
      <li>A todos os trabalhos premiados. </li>
      <li>Aos apresentadores (um certificado por trabalho apresentado).</li>
    </ol>
  </li>
  <li>Situações que não estejam previstas no presente regulamento serão resolvidas pela Comissão Científica do V Simpósio Médico.</li>
</ul>

<h2>Normas para confecção dos Pôsteres</h2>

<ul>
  <li>O pôster deve conter o título, nome e instituição dos autores.</li>
  <li>O nome do apresentador deverá estar sublinhado. O endereço eletrônico de um dos autores devera ser informado.</li>
  <li>Cada apresentador poderá apresentar até dois pôsteres no evento.</li>
  <li>O título do pôster deverá ser o mesmo do resumo aceito </li>
  <li>Poderão ser incluídos recursos para despertar o interesse do público e facilitar a explicação dos resultados (tabelas, gráficos, fotos e informações adicionais)</li>
  <li>Medida do pôster: 0,90m x 1,20m (largura x altura); </li>
  <li>Deverão incluir os tópicos: introdução, objetivo(s), materiais e métodos, resultados e discussão, conclusões e referências. O tipo da fonte deve ser simples, permitindo leitura rápida. </li>
  <li>É obrigatória a apresentação do pôster. </li>
  <li>Todos os pôsteres deverão ser afixados no dia 01/09/2010 a partir das 19 horas e deverão permanecer em exposição durante todo o simpósio.</li> 
  <li>Os trabalhos serão apresentados em data e horário a serem definidos pela organização do Simpósio. </li>
  <li>A fixação do pôster no espaço a ele destinado é de total responsabilidade dos autores, no primeiro dia do evento, sob orientação da comissão organizadora.</li>
  <li>A comissão organizadora não é responsável por trabalhos não retirados ou extraviados;</li>
  <li>Sugere-se preparar cópias do trabalho, ou cópias reduzidas do painel, e deixar disponível para os interessados.</li>  
</ul>

<h2>Normas para Apresentação Oral:</h2>

<ul>
  <li>A apresentação deve ser montada em PowerPoint.</li>
  <li>O tempo para apresentação oral será de 10 minutos e mais 5 minutos para perguntas.</li>
  <li>Deverão incluir os tópicos: introdução, objetivo(s), material e métodos, resultados, discussão, conclusões e referências</li>
  <li>Os trabalhos serão apresentados em data e horário a serem definidos pela organização do Simpósio.</li>
  <li>O Simpósio disponibilizará aos apresentadores computador e projetor multimídia (datashow) para a apresentação.</li>
  <li>Cabe ao apresentador trazer o material com sua apresentação (em pen drive ou cd-rom).</li>
<ul>
            
