                  <h1>Resumo</h1>
                  <p style="text-align:justify">
                    O curso de Medicina exige uma preparação mínima de 6 anos para a formação de um "médico generalista", além de alguns anos mais de estudo na Residência Médica. Ora, será que todo esse tempo de dedicação é capaz de fornecer ao aspirante médico o conhecimento necessário para a vivência profissional? Espera-se que sim. Contudo, é de demasia importância que o acadêmico preencha certas "lacunas" existentes durante esse percurso, tais como situações em que o rigor teórico das aulas o impeça de aprender, ou por falta de tempo em sala de aula. Logo, O "V Simpósio Médico-A Construção do Profissional da Saúde", tem o objetivo de oferecer ao acadêmico informações acerca de diversos temas importantes não englobados efetivamente durante as aulas, confrontá-lo com experiências diferentes das vividas no ambiente universitário, prepará-lo para intercorrências advindas do exercício da profissão, discutir e promover melhoras na saúde mental do acadêmico de Medicina, além de estreitar os laços entre os diversos profissionais da Saúde. Para tal, serão usadas palestras, mesas redondas e conferências ministradas por médicos, farmacêuticos, dentre outros. Espera-se a participação não só dos acadêmicos de Medicina, mas de futuros profissionais da Saúde de todas as áreas.
                  </p>
                  <h1>Objetivos</h1>	
                  <p style="text-align:justify">
                    O "V Simpósio Médico-A Construção do Profissional da Saúde" almeja a participação maciça dos acadêmicos da UNIOESTE. Espera-se que as informações obtidas com o Simpósio complementem o conhecimento de sala de aula, forneça ao acadêmico melhorias na relação médico & paciente e com os demais profissionais da Saúde, e o ajude na futura vivência médica. Afinal, ser médico não é apenas graduar-se em Medicina ou fazer a Residência Médica, requer ainda experiência na prática médica, confiança e respeito no trabalho, e acima de tudo, dedicação. Assim, são pequenas parcelas de contribuição, como o simpósio, que vão, aos poucos, solidificando a estrutura do futuro médico. Contamos com o apoio da UNIOESTE - Campus de Cascavel e com a iniciativa privada para a realização do evento.  
                  </p>
