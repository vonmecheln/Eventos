                  <h1>Apresenta&ccedil;&atilde;o</h1>
                  <p>O VI Congresso de Odontologia da Unioeste (VI COU), que re&uacute;ne a XI Jornada Acad&ecirc;mica de Odontologia da Unioeste e o VI Encontro de Ex-Alunos, ser&aacute; um grande evento da Odontologia da regi&atilde;o Oeste do Paran&aacute;. <br />
                    Frente &agrave; velocidade de desenvolvimento da ci&ecirc;ncia odontol&oacute;gica e da situa&ccedil;&atilde;o da Odontologia, torna-se fundamental oferecer aos profissionais, acad&ecirc;micos e demais profissionais relacionados &agrave; &aacute;rea, a oportunidade de atualiza&ccedil;&atilde;o e aprimoramento de conhecimentos, al&eacute;m de ser um importante instrumento de socializa&ccedil;&atilde;o e uni&atilde;o da classe odontol&oacute;gica. <br />
