<H1 STYLE='TEXT-ALIGN:center;'>NORMAS PARA  INSCRIÇÃO E APRESENTAÇÃO DE TRABALHOS</h1>

<p align="justify">      <font size="2" face="Arial">Serão 
aceitos trabalhos nas categorias Pesquisa/Extensão, Caso Clínico e 
Revisão da Literatura, nas formas de apresentação oral ou painel. 
Os trabalhos deverão ser inscritos pelo site até o dia 05/09/2009.</font></p>
<p align="justify">      <font size="2" face="Arial">O 
autor deverá preencher todos os campos da ficha de inscrição de trabalhos 
e assinalar a categoria, a área de conhecimento, forma de apresentação 
(oral ou painel) e opção graduação ou profissional. Não haverá 
limites de trabalhos para um mesmo autor, podendo participar como co-autor 
em até três trabalhos, independentemente da categoria. Para orientadores 
não há limite de trabalhos. Nas categorias <b>Caso Clínico</b> e <b>
Revisão de literatura, </b>cada trabalho poderá ter, no máximo, 1 
(um) apresentador, 3 (três) co-autores e 2 (dois) orientadores. Nas 
categorias <b>Pesquisa/Extensão, </b>

não há limites de autores.</font></p>
<p align="justify">      <font size="2" face="Arial">O 
apresentador deverá enviar o comprovante digital de pagamento da taxa 
de inscrição para que o seu trabalho seja incluído na programação 
e nos anais, caso seja selecionado pela Comissão Científica.</font></p>
<p align="justify">      <font size="2" face="Arial">Os 
trabalhos poderão ser encaminhados nas formas de trabalho completo 
ou de resumo, conforme normas descritas a seguir. Antes da sua submissão, 
recomenda-se que sejam criteriosamente revisados pelos autores, principalmente 
pelo orientador. </font> <br></p>
<p align="justify"><font size="2" face="Arial"><b>1. 
Trabalho completo</b></font></p>
<p align="justify">      <font size="2" face="Arial">Os 
autores que optarem por "Trabalho Completo", deverão enviar um 
texto inédito, contendo entre 5 (cinco) e 10 (dez) páginas, digitado 
em formato A4, fonte Arial tamanho 11, em espaço simples, justificado, 
com margens superior, inferior, direita e esquerda de 3,0 cm. Deve conter 
os seguintes itens: título em caixa alta e negrito; nome dos autores 
(por extenso) alinhados à direita com o nome do apresentador sublinhado 
e em 1º lugar; em nota de rodapé, identificar a titulação, instituição, 
e e-mail do apresentador; resumo de 200 a 250 palavras; três a quatro 
palavras-chave; texto contendo:</font></p>
<p align="justify"><font size="2" face="Arial"><u>Pesquisa/Extensão</u>: 
introdução, objetivos, metodologia, resultados, discussão, conclusões 
e referências bibliográficas (conforme normas da ABNT). Os subtítulos 
devem ser em negrito, justificados à esquerda da página e separados 
por um espaço do texto anterior.</font></p>

<p align="justify"><font size="2" face="Arial"><u>Caso Clínico</u>: 
introdução, objetivos, relato do caso, discussão, conclusões e referências 
bibliográficas (conforme normas da ABNT). Os subtítulos devem ser 
em negrito, justificados à esquerda da página e separados por um espaço 
do texto anterior.</font></p>
<p align="justify"><font size="2" face="Arial"><u>Revisão de literatura</u>: 
introdução, objetivos, revisão de literatura, discussão, conclusão 
e referências bibliográficas (conforme normas da ABNT). Os subtítulos 
devem ser em negrito, justificados à esquerda da página e separados 
por um espaço do texto anterior.</font> <br> <br></p>
<p align="justify"><font size="2" face="Arial"><b>2. 
Resumo </b></font></p>
<p align="justify">      <font size="2" face="Arial">Os 
autores que optarem por "Resumo", deverão enviar um resumo estruturado 
de 1000 a 1500 caracteres, digitado em formato A4, fonte Arial tamanho 
11, em espaço simples, justificado, com margens superior, inferior, 
direita e esquerda de 3,0 cm. Deve conter os seguintes itens: título 
em caixa alta e negrito; forma de apresentação, categoria, área e 
opção (seguindo as informações do formulário de inscrição no 
site); nome dos autores (por extenso) alinhados à direita com o nome 
do apresentador sublinhado e em 1º lugar; em nota de rodapé, identificar 
a titulação, instituição, e e-mail do apresentador, além de: </font></p>
<p align="justify"><font size="2" face="Arial"><u>Pesquisa/Extensão</u>: 
introdução, objetivos, metodologia, resultados e conclusões;</font></p>
<p align="justify"><font size="2" face="Arial"><u>Caso Clínico</u>: 
introdução, objetivos, relato do caso e conclusões;</font></p>

<p align="justify"><font size="2" face="Arial"><u>Revisão de literatura</u>: 
introdução, objetivos, revisão de literatura, discussão e conclusão.</font></p>
<p align="justify">      <font size="2" face="Arial">Deverão 
ser apresentadas duas a três referências bibliográficas (conforme 
normas da ABNT) e três a quatro palavras-chave.</font> <br></p>
<p align="justify"><font size="2" face="Arial"><b>É importante lembrar 
que para os trabalhos de Pesquisa que envolverem seres humanos, 
seja em forma de artigo completo ou resumo, 
é <u>obrigatóri</u>o apresentar o 
parecer de aprovação do Comitê de 
Ética em Pesquisa na forma de figura, no final do arquivo do artigo 
completo ou resumo a ser enviado.</b></font> <br></p>
<p align="justify"><font size="2" face="Arial"><b>Painel</b></font></p>
<p align="justify">      <font size="2" face="Arial">O 
painel deverá ser elaborado nas seguintes dimensões máximas: 90 cm 
de largura e 1,60 metro de altura. É de responsabilidade dos autores 
a fixação dos mesmos no dia 19 de agosto (quarta-feira) das 9:30 às 
12:00h, sendo obrigatória a presença do apresentador junto ao painel 
no dia 21 de agosto (sexta-feira) das 9:00 às 12:00h, para avaliação. 
O título do painel deve ser exatamente o mesmo utilizado no trabalho 
enviado e ser escrito em letras maiúsculas. Abaixo do título, em fontes 
menores, devem aparecer os nomes dos autores, departamento, instituição, 
cidade e estado. O nome do apresentador deverá estar sublinhado. O 
painel deve ser auto-explicativo, de preferência com o mínimo possível 
de texto e o máximo de ilustrações (figuras, diagramas e tabelas). 
Sugere-se que as conclusões sejam colocadas na forma de itens. Não 
é obrigatória a inserção de abstract. <br>

</font></p>
<p><font size="2" face="Arial"><b>Apresentação Oral </b></font></p>
<p align="justify">      <font size="2" face="Arial">Após 
o tempo de apresentação (idealmente entre 12 e 15 minutos), haverá 
5 minutos para discussão.</font></p>
<p align="justify">      <font size="2" face="Arial">O 
apresentador terá à sua disposição: 1 (um) projetor multimídia 
e ponteira a laser.</font> <br></p>
<p align="justify"><font size="2" face="Arial"><b>AVALIAÇÃO</b></font></p>
<p align="justify">      <font size="2" face="Arial">Os 
trabalhos serão avaliados de acordo com a qualidade da apresentação/material 
audiovisual, conteúdo, didática, argüição e tempo, sendo que o 
mesmo deve ficar, idealmente, entre 5 e 8 minutos para apresentação 
dos painéis e 12 e 15 minutos para apresentação oral.</font> <br>
</p>

<p align="justify"><font size="2" face="Arial"><b>DIVULGAÇÃO 
DOS TRABALHOS SELECIONADOS</b></font></p>
<p align="justify">      <font size="2" face="Arial">A 
Comissão Científica do COU divulgará os resultados da análise dos 
trabalhos até o dia 16/09/2009, no site do evento.</font></p>
<p align="justify">      <font size="2" face="Arial">Os 
trabalhos selecionados serão publicados nos Anais do VI Congresso de 
Odontologia da UNIOESTE.</font></p>
<p align="justify">      <font size="2" face="Arial">Os 
melhores trabalhos de cada categoria receberão menção honrosa.</font> <br>
</p>
